#include <iostream>
using namespace std;

/** isGreatherThan
 * @brief returns whether x is greater than y
 *
 * @param x : (double)
 * @param y : (int)
 * @return true : x is greater than y
 * @return false : x is less than or equal to y
 */

 bool isGreaterThan(double x, int y) {
    if (x > y) {
        return true;
    } else{
        return false;
    }
 }
    

